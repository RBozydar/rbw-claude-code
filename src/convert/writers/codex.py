from __future__ import annotations

import os

from ..converters.content import transform_content_for_codex
from ..types import ClaudeMcpServer, CodexBundle
from .files import backup_file, copy_skill_dir, ensure_dir, sanitize_path_name, write_text


def write_codex_bundle(output_root: str, bundle: CodexBundle) -> None:
    paths = _resolve_codex_paths(output_root)
    ensure_dir(paths["root"])

    if bundle.prompts:
        ensure_dir(paths["prompts_dir"])
        for prompt in bundle.prompts:
            write_text(
                os.path.join(paths["prompts_dir"], f"{prompt.name}.md"),
                prompt.content + "\n",
            )

    if bundle.skill_dirs:
        ensure_dir(paths["skills_dir"])
        for skill in bundle.skill_dirs:
            copy_skill_dir(
                skill.source_dir,
                os.path.join(paths["skills_dir"], sanitize_path_name(skill.name)),
                lambda content: transform_content_for_codex(
                    content,
                    prompt_targets=(
                        bundle.invocation_targets.prompt_targets
                        if bundle.invocation_targets
                        else None
                    ),
                    skill_targets=(
                        bundle.invocation_targets.skill_targets
                        if bundle.invocation_targets
                        else None
                    ),
                    agent_targets=(
                        bundle.invocation_targets.agent_targets
                        if bundle.invocation_targets
                        else None
                    ),
                    unknown_slash_behavior="preserve",
                ),
            )

    if bundle.generated_skills:
        ensure_dir(paths["skills_dir"])
        for skill in bundle.generated_skills:
            write_text(
                os.path.join(
                    paths["skills_dir"], sanitize_path_name(skill.name), "SKILL.md"
                ),
                skill.content + "\n",
            )

    config = render_codex_config(bundle.mcp_servers)
    if config:
        backup_path = backup_file(paths["config_path"])
        if backup_path:
            print(f"Backed up existing config to {backup_path}")
        write_text(paths["config_path"], config)


def render_codex_config(
    mcp_servers: dict[str, ClaudeMcpServer] | None,
) -> str | None:
    if not mcp_servers:
        return None

    lines: list[str] = ["# Generated by convert", ""]
    for name, server in mcp_servers.items():
        key = _format_toml_key(name)
        lines.append(f"[mcp_servers.{key}]")

        if server.command:
            lines.append(f"command = {_format_toml_string(server.command)}")
            if server.cwd:
                lines.append(f"cwd = {_format_toml_string(server.cwd)}")
            if server.args:
                args = ", ".join(_format_toml_string(arg) for arg in server.args)
                lines.append(f"args = [{args}]")
            if server.env:
                lines.append("")
                lines.append(f"[mcp_servers.{key}.env]")
                for env_key, value in server.env.items():
                    lines.append(
                        f"{_format_toml_key(env_key)} = {_format_toml_string(value)}"
                    )
        elif server.url:
            lines.append(f"url = {_format_toml_string(server.url)}")
            if server.headers:
                lines.append(
                    f"http_headers = {_format_toml_inline_table(server.headers)}"
                )

        lines.append("")

    return "\n".join(lines)


def _resolve_codex_paths(output_root: str) -> dict[str, str]:
    if os.path.basename(output_root) == ".codex":
        root = output_root
    else:
        root = os.path.join(output_root, ".codex")
    return {
        "root": root,
        "config_path": os.path.join(root, "config.toml"),
        "prompts_dir": os.path.join(root, "prompts"),
        "skills_dir": os.path.join(root, "skills"),
    }


def _format_toml_string(value: str) -> str:
    escaped = value.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{escaped}"'


def _format_toml_key(value: str) -> str:
    return value if value.replace("_", "").replace("-", "").isalnum() else _format_toml_string(value)


def _format_toml_inline_table(entries: dict[str, str]) -> str:
    parts = [
        f"{_format_toml_key(key)} = {_format_toml_string(value)}"
        for key, value in entries.items()
    ]
    return "{ " + ", ".join(parts) + " }"
